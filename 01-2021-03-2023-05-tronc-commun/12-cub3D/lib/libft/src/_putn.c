#include <stddef.h>

void	_putc(char c);

void	_putn(long n)
{
	if (n < 0)
	{
		_putc('-');
		n *= -1;
	}
	if (n < 10)
		_putc(n + '0');
	else
	{
		_putn(n / 10);
		_putc((n % 10) + '0');
	}
}
