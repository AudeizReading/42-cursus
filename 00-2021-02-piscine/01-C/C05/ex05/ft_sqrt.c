int		ft_sqrt(int nb)
{
	double	a1;
	int		i;

	i = 0;
	if (nb < 0 || nb == 0)
		return (0);
	while (i <= 50)
	{
		if (!i)
			a1 = nb;
		else
			a1 = ((a1 + nb / a1) / 2);
		i++;
	}
	if ((a1 - (int)a1) == 0)
		return ((int)a1);
	else
		return (0);
}
