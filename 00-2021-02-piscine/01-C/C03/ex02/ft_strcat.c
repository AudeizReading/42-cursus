char		*ft_strcat(char *dest, char *src)
{
	char	*p_dest;

	p_dest = dest;
	while (*p_dest)
	{
		p_dest++;
	}
	while (*src)
	{
		*p_dest++ = *src++;
	}
	*p_dest = '\0';
	return (dest);
}
