#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	argv++;
	while (--argc > 0)
	{
		ft_putstr(*argv++);
		ft_putstr("\n");
	}
	return (0);
}
