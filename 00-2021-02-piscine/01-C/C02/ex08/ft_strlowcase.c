char		*ft_strlowcase(char *str)
{
	char	*p_str;

	p_str = str;
	while (*p_str != '\0')
	{
		if (*p_str >= 65 && *p_str <= 90)
			*p_str += 32;
		p_str++;
	}
	return (str);
}
