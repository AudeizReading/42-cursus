char		*ft_strupcase(char *str)
{
	char	*p_str;

	p_str = str;
	while (*p_str != '\0')
	{
		if (*p_str >= 97 && *p_str <= 122)
			*p_str -= 32;
		p_str++;
	}
	return (str);
}
